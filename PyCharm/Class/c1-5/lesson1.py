b = str(input('Введите числовое/символьное значение для переменной (b): '))
a,b = b,a
'''(комментарий для себя) При выполнении a, b = b, a интерпретатор Python сначала получает 
значения связанные с переменными b и a (правая часть) и помещает их в кортеж, 
в данном случае получится (10, 20). После этого он связывает каждый элемент кортежа 
в определенной позиции с переменной в той же позиции, но в кортеже слева (a,b).
Таким образом можно поменять значения не только двух переменных, но и трех, четырех и т. д. 
Кроме того в Python можно обменять значения переменных разных типов. 
Такая возможность связана с тем, что тип данных в Питоне привязан не к переменной, а к значению'''
